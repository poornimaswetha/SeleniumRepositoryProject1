package Wrapperclass;

public class Wrapperclass {

	public static void main(String[] args) {
		// Wrapper class 
		// Primitive datatypes When used as Objects
		Integer myint=45; //int pridatatype is Integer Wrapperclass object
		Double mydob=123.45;//double pridatatype is Double Wrapperclass object
		Character mychar='F';// Char pridatatype is Character Wrapperclass object
				
		// To get the value of wrapper object
		System.out.println(myint.intValue());
		System.out.println(mydob.doubleValue());
		System.out.println(mychar.charValue());
		
	}

}
